<h1 id="overview">Overview</h1>
<p>Learn what patterns are in Sveltique and how they can help you build interfaces faster.</p>

<h2 id="what-are-patterns">What are patterns ?</h2>
<p>
    Patterns are practical examples of how to combine Sveltique components into real interface elements.
    They go beyond individual components by demonstrating how to connect them with state, events, and
    accessibility in mind.
</p>
<p>
    Each pattern focuses on a specific problem, such as building <Link href="/docs/patterns/browse/programmatic-toasts">
        programmatic toasts
    </Link>, or a custom <Link href="/docs/patterns/browse/context-menu">context menu</Link>.
</p>
<Alert>
    Some patterns may eventually become part of the core library.
</Alert>

<h2 id="contributing">Contributing</h2>
<p>
    Patterns are a growing collection. If you have ideas for new patterns or improvements, you can
    <Link href="https://github.com/sveltique/sveltique" external>suggest them</Link>.
</p>
