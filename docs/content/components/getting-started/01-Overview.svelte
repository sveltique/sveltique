<h1 id="overview">Overview</h1>
<p>Build accessible, customizable UI components for Svelte.</p>

<h2 id="what-is-sveltique">What is Sveltique</h2>
<p>
    <strong>Sveltique</strong> is a component library for Svelte designed to make building accessible interfaces effortless.
    Every component ships with sensible defaults, complete keyboard and screen reader support, and full customization when you need it.
</p>
<p>
    Whether you're prototyping quickly or maintaining a large-scale app, Sveltique helps you stay consistent and accessible without extra boilerplate.
</p>

<h2 id="core-principles">Core principles</h2>
<ul>
    <li>
        Accessible by default : Every component follows WAI-ARIA and keyboard interaction standards.
        Before building a new componet, we try to use existing standards.
    </li>
    <li>
        Customizable : Easy to style or extend with your own variants and utilities.
    </li>
    <li>
        Lightweight : Minimal runtime cost, no external dependencies.
    </li>
</ul>

<h2 id="key-features">Key features</h2>
<ul>
    <li>Theming through Tailwind CSS</li>
    <li>Motion-safe animations</li>
    <li>Type-safe, ergonomic APIs</li>
    <li>Ready for Svelte and SvelteKit projects</li>
</ul>

<h2 id="design-goals">Design goals</h2>
<p>
    Sveltique draws inspiration from libraries like <Badge>Radix UI</Badge>, <Badge>shadcn</Badge>,
    and <Badge>Flowbite</Badge>. It aims to provide the same level of robustness and accessibility while
    keeping the developer experience simple and declarative.
</p>
